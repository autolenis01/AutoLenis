/**
 * Escape HTML special characters to prevent XSS when embedding
 * user-supplied values in HTML email templates or other HTML contexts.
 */
export function escapeHtml(value: string): string {
  return value
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#039;")
}
